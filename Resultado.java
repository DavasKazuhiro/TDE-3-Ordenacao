public class Resultado {
    public String tipo;
    public String numero;
    public int iteracoes;
    public int trocas;

    public Resultado(String tipo, String numero, int iteracoes, int trocas) {
        this.tipo = tipo;
        this.numero = numero;
        this.iteracoes = iteracoes;
        this.trocas = trocas;
    }
}
